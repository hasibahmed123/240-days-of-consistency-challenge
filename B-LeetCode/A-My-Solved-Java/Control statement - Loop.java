// 1.for loop Formate:
/*
for (initialization; condition; increment/decrement) {
    // code block to be executed
}

for (int i = 0; i < 5; i++) {
  System.out.println(i);
}
 */

// 2.while loop Formate:
/*
while (condition) {
  // code block to be executed
}

int i = 0;

while (i < 5) {
  System.out.println(i);
  i++;
}
 */
 
//for loop example with full code
public class ForLoopExample {
    public static void main(String[] args) {
        for (int i = 0; i < 5; i++) {
            System.out.println(i);
        }
    }
}

//while loop example with full code
public class WhileLoopExample {
    public static void main(String[] args) {
        int i = 0;
        while (i < 5) {
            System.out.println(i);
            i++;
        }
    }
}

