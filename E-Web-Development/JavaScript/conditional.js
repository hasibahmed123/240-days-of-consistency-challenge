// Conditional statements

console.log(5>10);
console.log(5<10);
console.log(5>=10);
console.log(5<=10);
console.log(5==10);
console.log(5!=10);
console.log(5===10);
console.log(5!==10);
console.log(5>10 && 10<20); // this means that both 5 is greater than 10 and 10 is less than 20
console.log(5>10 || 10<20); //this means that either 5 is greater than 10 or 10 is less than 20

//---------------------------------------------------

var mangoPrice = 10;
var applePrice = 20;
console.log(mangoPrice > applePrice); // false
console.log(mangoPrice < applePrice); // true
console.log(mangoPrice >= applePrice); // false
console.log(mangoPrice <= applePrice); // true

// use of == and ===
// == checks for value only
// === checks for value and type
console.log(5 == '5'); // true, because value is same
console.log(5 === '5'); // false, because type is different (number vs string

















