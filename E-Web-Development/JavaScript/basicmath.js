// Mathematical operations

//sum
var price = 56;
price = price + 44; // 100
// or
price += 44; // 100
console.log(price);

//subtraction
var price = 100;
price = price - 44; // 56
// or
price -= 44; // 56
console.log(price);

//multiplication
var price = 56;
price = price * 2; // 112
// or
price *= 2; // 112
console.log(price);

//division
var price = 112;
price = price / 2; // 56
// or
price /= 2; // 56
console.log(price);

//modulus
var price = 56;
price = price % 5; // 1 (56 divided by 5 gives a remainder of 1)
// or
price %= 5; // 1
console.log(price);

// adding strings
var firstName = "John";
var lastName = "Doe";
var fullName = firstName + " "+ lastName; // "John Doe"
console.log(fullName);
